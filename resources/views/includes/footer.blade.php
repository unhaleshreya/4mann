<script>
    function updateBreadcrumbBackground() {
        let breadcrumb = document.querySelector('.breadcumb-area');
        if (!breadcrumb) return;
        let desktopImage = breadcrumb.getAttribute('data-desktop');
        let mobileImage = breadcrumb.getAttribute('data-mobile');

        if (window.innerWidth <= 768) {
            breadcrumb.style.backgroundImage = `url(${mobileImage})`;
        } else {
            breadcrumb.style.backgroundImage = `url(${desktopImage})`;
        }
    }

    updateBreadcrumbBackground();
    window.addEventListener('resize', updateBreadcrumbBackground);
</script>

<script>
    document.addEventListener('DOMContentLoaded', function() {
        const filters = document.querySelectorAll('.filter');
        const galleryItems = document.querySelectorAll('.gallery-item');

        function applyFilters() {
            const activeFilters = Array.from(filters)
                .filter(f => f.checked)
                .map(f => f.dataset.filter);

            galleryItems.forEach(item => {
                const classes = item.className.split(' ');
                const match = activeFilters.every(f => classes.includes(f));
                item.style.display = match ? 'block' : 'none';
            });
        }

        filters.forEach(filter => {
            filter.addEventListener('change', applyFilters);
        });

        applyFilters();
    });
</script>

