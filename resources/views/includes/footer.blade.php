<script>
    const imgAfter = document.querySelector('.custom-after');
    const line = document.querySelector('.custom-line');
    const slider = document.querySelector('.custom-slider-range');

    if (slider && imgAfter && line) {
        slider.addEventListener('input', () => {
            line.style.left = `${slider.value}%`;
